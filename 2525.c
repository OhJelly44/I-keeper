// 수정 답안

#include <stdio.h>

int main()

{
int 시, 분, 시간;
printf("몇 시 몇 분인지 걸리는 시간을 순서대로 입력하세요:");
scanf_s("%d %d %d", &시, &분, &시간);

// 입력된 시간과 분에 시간을 더함
시 += 시간 / 60; // 시간을 시로 변환하여 더함
분 += 시간 % 60; // 시간을 분으로 변환하여 더함

// 분이 60분을 넘어가면 시간으로 넘김
if (분 >= 60) {
	시 += 분 / 60;
	분 %= 60;
}

// 시간이 24시를 넘어가면 하루를 더함
if (시 >= 24) {
	시 %= 24;
}
printf("%d시 %d분", 시, 분);
return 0;
}
